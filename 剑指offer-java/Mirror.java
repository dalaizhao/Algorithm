/*
 * 题目描述
 *	操作给定的二叉树，将其变换为源二叉树的镜像。 
 *	输入描述:
 *	二叉树的镜像定义：源二叉树 
    	    8
    	   /  \
    	  6   10
    	 / \  / \
    	5  7 9 11
    	镜像二叉树
    	    8
    	   /  \
    	  10   6
    	 / \  / \
    	11 9 7  5
  * */

public class Mirror {

	// 二叉树的镜像
	public void mirror(TreeNode root) {

		if (root == null) {
			return;
		}
		TreeNode tmpNode = root.left;
		root.left = root.right;
		root.right = tmpNode;

		if (root.left != null) {
			mirror(root.left);
		}
		if (root.right != null) {
			mirror(root.right);
		}
	}

	public class TreeNode {
		int val;
		TreeNode left;
		TreeNode right;

		TreeNode(int x) {
			val = x;
		}
	}

}
